/**
 El Single Responsibility Principle (Principio de Responsabilidad Única)(SRP)
  establece que una clase debe tener una única responsabilidad, 
 es decir, una sola razón para cambiar. Esto implica que la clase debe estar enfocada en una sola tarea
  o funcionalidad, lo que mejora la cohesión del código y facilita su mantenimiento.
   En el acrónimo SOLID, la S se refiere precisamente a este principio,
    donde la "razón para cambiar" es identificada como "responsabilidad".
 */
